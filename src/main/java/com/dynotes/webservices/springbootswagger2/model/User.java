package com.dynotes.webservices.springbootswagger2.model;

/**
 * @author Yufeng Chen
 */
public class User {

	private String name;
    private int age;
    private String email;
    
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
    
    
}
